package com.leige.design;

/**
 * 创建型模式
 * - 提供了一种在创建对象的同时隐藏创建逻辑的方式，而不是使用new运算符直接实例化对象。
 *   这使得程序在判断针对某个给定实例需要创建哪些对象时更加灵活。
 *
 * 结构型模式
 * - 关注类和对象的组合。继承的概念被用来组合接口和定义组合对象获得新功能的方式。
 *
 * 行为型模式
 * - 特别关注对象之间的通信。
 */
public class Design {
    /**
     * 创建型模式 - 5种
     * 工厂模式（Factory Pattern）
     * 抽象工厂模式（Abstract Factory Pattern）
     * 单例模式（Singleton Pattern）
     * 建造者模式（Builder Pattern）
     * 原型模式（Prototype Pattern）
     */
    /**
     * 结构型模式 - 8种
     * 适配器模式（Adapter Pattern）
     * 桥接模式（Bridge Pattern）
     * 过滤器模式（Filter、Criteria Pattern）
     * 组合模式（Composite Pattern）
     * 装饰器模式（Decorator Pattern）
     * 外观模式（Facade Pattern）
     * 享元模式（Flyweight Pattern）
     * 代理模式（Proxy Pattern）
     */
    /**
     * 行为型模式 - 11种
     * 责任链模式（Chain of Responsibility Pattern）
     * 命令模式（Command Pattern）
     * 解释器模式（Interpreter Pattern）
     * 迭代器模式（Iterator Pattern）
     * 中介者模式（Mediator Pattern）
     * 备忘录模式（Memento Pattern）
     * 观察者模式（Observer Pattern）
     * 状态模式（State Pattern）
     * 策略模式（Strategy Pattern）
     * 模板模式（Template Pattern）
     * 访问者模式（Visitor Pattern）
     */
    /**
     * 设计模式之七大基本原则
     * 1。开闭原则ocp：面向扩展开放，面向修改关闭
     * 2。单一职责：每一个类应该专注于做一件事情。
     * 3。里氏替换：超类存在的地方，子类是可以替换的
     * 4。依赖倒置：实现尽量依赖抽象，不依赖具体实现
     * 5。接口隔离：提供尽可能小的单独的接口，而不是提供大的总的接口
     * 6。最少知识原则/迪米特法则：一个软件实体应当尽可能少的与其他实体发生相互作用
     * 7。合成复用：多用组合，少用继承。（一个类中有另一个类的对象）
     */
}
